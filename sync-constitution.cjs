/**
 * sync-constitution.cjs — Synchronize constitution.json → derived artifacts.
 *
 * Reads constitution.json and generates/updates:
 *   1. constitution.md (human-readable governance backbone)
 *   2. .specify/memory/constitution.md (human-readable governance backbone)
 *
 * Usage:
 *   node sync-constitution.cjs            # sync from constitution.json
 *   node sync-constitution.cjs --check    # exit 1 if derived docs are stale
 *
 * This script uses CommonJS to avoid ESM/TypeScript build dependency.
 */
'use strict';

const fs = require('node:fs');
const path = require('node:path');

const ROOT = path.resolve(__dirname);
const CONSTITUTION_PATH = path.join(ROOT, 'constitution.json');
const OUTPUT_FILES = [
  path.join(ROOT, 'constitution.md'),
  path.join(ROOT, '.specify', 'memory', 'constitution.md')
];

function loadConstitution() {
  if (!fs.existsSync(CONSTITUTION_PATH)) {
    console.error(`[sync-constitution] ERROR: ${CONSTITUTION_PATH} not found.`);
    process.exit(1);
  }
  return JSON.parse(fs.readFileSync(CONSTITUTION_PATH, 'utf-8'));
}

function generateMarkdown(c) {
  const lines = [];
  lines.push(`# ${c.name || 'Project'} Constitution`);
  lines.push('');
  if (c.description) {
    lines.push(`> ${c.description}`);
    lines.push('');
  }
  lines.push(`**Version**: ${c.version || '0.0.0'}  `);
  lines.push(`**Ratified**: ${c.ratifiedAt || 'TBD'}`);
  lines.push('');

  lines.push('## Core Principles');
  lines.push('');

  const articles = c.articles || {};
  let idx = 1;
  const numerals = ['I', 'II', 'III', 'IV', 'V', 'VI', 'VII', 'VIII', 'IX', 'X'];

  for (const [key, article] of Object.entries(articles)) {
    const numeral = numerals[idx - 1] || `${idx}`;
    const title = article.title || key.charAt(0).toUpperCase() + key.slice(1);
    lines.push(`### ${numeral}. ${title}`);
    lines.push('');

    if (article.rules && article.rules.length > 0) {
      lines.push('| ID | Rule | Severity |');
      lines.push('|----|------|----------|');
      for (const r of article.rules) {
        lines.push(`| ${r.id} | ${r.rule} | ${r.severity} |`);
      }
      lines.push('');
    }
    idx++;
  }

  // Thresholds
  if (c.thresholds) {
    lines.push('## Thresholds');
    lines.push('');
    lines.push('| Metric | Value |');
    lines.push('|--------|-------|');
    for (const [k, v] of Object.entries(c.thresholds)) {
      lines.push(`| ${k} | ${v} |`);
    }
    lines.push('');
  }

  // References
  if (c.references) {
    lines.push('## Key References');
    lines.push('');
    for (const [k, v] of Object.entries(c.references)) {
      lines.push(`- **${k}**: \`${v}\``);
    }
    lines.push('');
  }

  lines.push('---');
  lines.push(`*Generated by sync-constitution.cjs from constitution.json v${c.version || '?'}*`);
  lines.push('');

  return lines.join('\n');
}

function main() {
  const checkOnly = process.argv.includes('--check');
  const constitution = loadConstitution();
  const newContent = generateMarkdown(constitution);

  if (checkOnly) {
    const staleOutputs = OUTPUT_FILES.filter((outputPath) => {
      const outDir = path.dirname(outputPath);
      if (!fs.existsSync(outDir)) return false; // skip if parent dir missing (e.g., .specify/ not published)
      const existing = fs.existsSync(outputPath) ? fs.readFileSync(outputPath, 'utf-8') : '';
      return existing.trim() !== newContent.trim();
    });

    if (staleOutputs.length > 0) {
      console.error(`[sync-constitution] STALE: ${staleOutputs.join(', ')} out of sync with constitution.json`);
      console.error('[sync-constitution] Run: node sync-constitution.cjs');
      process.exit(1);
    }
    console.log('[sync-constitution] OK: derived docs are in sync.');
    return;
  }

  for (const outputPath of OUTPUT_FILES) {
    const outDir = path.dirname(outputPath);
    if (!fs.existsSync(outDir)) {
      fs.mkdirSync(outDir, { recursive: true });
    }
    fs.writeFileSync(outputPath, newContent, 'utf-8');
    console.log(`[sync-constitution] Generated ${outputPath}`);
  }
}

main();
